int cmp_le(int x, int y) {
	return x <= y;
}

int cmp_l(int x, int y) {
	return x < y;
}

int cmp_g(int x, int y) {
	return x > y;
}

int cmp_ge(int x, int y) {
	return x >= y;
}

int cmp_ui_l(unsigned x, int y) {
	return x < y;
}

int cmp_u_l(unsigned x, unsigned y) {
	return x < y;
}

int cmp_uc_l(unsigned char x, char y) {
	return x < y;
}
